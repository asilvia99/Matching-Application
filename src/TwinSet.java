public class TwinSet {
    String little1;
    String little2;
    String big;
    int groupScore;
    int l1Score;
    int l2Score;

    TwinSet(String little1, String little2, String big, int groupScore, int l1Score, int l2Score){
        this.little1 = little1;
        this.little2 = little2;
        this.big = big;
        this.groupScore = groupScore;
        this.l1Score = l1Score;
        this.l2Score = l2Score;

    }


}
